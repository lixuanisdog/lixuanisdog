package com.example.zhinengjiaotong;

public class Bean_test3 {
    String name;
    String admin;
    String xinzhi;

    public Bean_test3(String name, String admin, String xinzhi) {
        this.name = name;
        this.admin = admin;
        this.xinzhi = xinzhi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAdmin() {
        return admin;
    }

    public void setAdmin(String admin) {
        this.admin = admin;
    }

    public String getXinzhi() {
        return xinzhi;
    }

    public void setXinzhi(String xinzhi) {
        this.xinzhi = xinzhi;
    }
}
